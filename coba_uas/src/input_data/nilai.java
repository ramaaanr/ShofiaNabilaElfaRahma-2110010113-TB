    private void tableTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTBMouseClicked
        // TODO add your handling code here:
        tambahButton.setEnabled(false);
        try {
            int pilih = tableTB.getSelectedRow();

            if (pilih != -1) {
                String npm = tableTB.getValueAt(pilih, 1).toString();
                String kode_krs = tableTB.getValueAt(pilih, 2).toString();
                String nilai = tableTB.getValueAt(pilih, 3).toString();
                String dosen = tableTB.getValueAt(pilih, 5).toString();
                String tanggal_input = tableTB.getValueAt(pilih, 4).toString();
                String keterangan = tableTB.getValueAt(pilih, 6).toString();

                npmCB.setSelectedItem(npm);
                kodeKrsCB.setSelectedItem(kode_krs);
                nilaiTF.setText(nilai);
                dosenCB.setSelectedItem(dosen);
                tanggalInputTF.setText(tanggal_input);
                keteranganTF.setText(keterangan);

                tambahButton.setEnabled(true); // Aktifkan tombol jika diperlukan
            } else {
                // Tampilkan pesan atau lakukan sesuatu jika tidak ada baris yang dipilih
            }
        } catch (Exception e) {
            e.printStackTrace();
            // Tampilkan pesan atau lakukan sesuatu jika terjadi kesalahan
        }
    }//GEN-LAST:event_tableTBMouseClicked

