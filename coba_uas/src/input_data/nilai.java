    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        String npm = npmCB.getSelectedItem().toString();
        String kode_krs = kodeKrsCB.getSelectedItem().toString();
        String dosen = dosenCB.getSelectedItem().toString();
        String nilai = nilaiTF.getText();
        String tanggal_input = tanggalInputTF.getText();
        String keterangan = keteranganTF.getText();

        if (npmCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "NPM harus dipilih");
        } else if (kodeKrsCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Kode KRS harus dipilih");
        } else if (dosenCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Dosen tidak harus dipilih");
        } else if (nilai.equals("")) {
            JOptionPane.showMessageDialog(null, "Nilai tidak boleh kosong");
        } else if (tanggal_input.equals("")) {
            JOptionPane.showMessageDialog(null, " Tanggal Input harus dipilih");
        } else if (keterangan.equals("")) {
            JOptionPane.showMessageDialog(null, " Keterangan harus dipilih");
        } else {
            try {
                new nilaiform().insert(npm, kode_krs, nilai, tanggal_input, dosen, keterangan);
                JOptionPane.showMessageDialog(this, "data berhasil dimasukkan");
                initTabel();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "data gagal dimasukkan karena " + e.getMessage());
            }
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

