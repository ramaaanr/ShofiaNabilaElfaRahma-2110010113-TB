    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableTB.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Data dipilih terlebih dahulu");
        } else {
            try {
                int id_nilai = Integer.parseInt(tableTB.getValueAt(selectedRow, 0).toString());
                new nilaiform().delete(id_nilai);
                JOptionPane.showMessageDialog(this, "data berhasil dihapus");
                initTabel();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "data gagal dihapus karena " + e.getMessage());
            }
        }
    }//GEN-LAST:event_hapusButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableTB.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Data dipilih terlebih dahulu");
        } else {
            String npm = npmCB.getSelectedItem().toString();
            String kode_krs = kodeKrsCB.getSelectedItem().toString();
            String dosen = dosenCB.getSelectedItem().toString();
            String nilai = nilaiTF.getText();
            String tanggal_input = tanggalInputTF.getText();
            String keterangan = keteranganTF.getText();
            int id_nilai = Integer.parseInt(tableTB.getValueAt(selectedRow, 0).toString());
            System.out.println(id_nilai);
            try {
                new nilaiform().update(id_nilai, npm, kode_krs, nilai, tanggal_input, dosen, keterangan);
                JOptionPane.showMessageDialog(this, "Data Berhasil Di Ubah");
                initTabel();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Data Gagal Di Ubah " + e.getMessage());
            }
        }

    }//GEN-LAST:event_editButtonActionPerformed

