    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        String npm = npmTextField.getText();
        String nama = namaTextField.getText();
        String jk = jkCB.getSelectedIndex() == 1 ? "L" : "P";
        String prodi = prodiCB.getSelectedItem().toString();
        String semester = semesterCB.getSelectedItem().toString();
        String program = programCB.getSelectedItem().toString();

        if (npmTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "nim tidak boleh kosong");
        } else if (namaTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "nama tidak boleh kosong");
        } else if (jkCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "alamat tidak boleh kosong");
        } else if (prodiCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "jenis kelamin harus dipilih");
        } else if (semesterCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, " jurusan harus dipilih");
        } else if (programCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, " jurusan harus dipilih");
        } else {
            try {
                new mahasiswaform().insert(npm, nama, jk, prodi, semester, program);
                JOptionPane.showMessageDialog(this, "data berhasil dimasukkan");
                initTabel();
                refresh();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "data gagal dimasukkan karena " + e.getMessage());
            }
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

