    private void tableTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTBMouseClicked
        // TODO add your handling code here:
        tambahButton.setEnabled(false);
        try {
            int pilih = tableTB.getSelectedRow();

            if (pilih != -1) {
                String npm = tableTB.getValueAt(pilih, 0).toString();
                String nama = tableTB.getValueAt(pilih, 1).toString();
                String jenis_kelamin = tableTB.getValueAt(pilih, 2).toString();
                String prodi = tableTB.getValueAt(pilih, 3).toString();
                String semester = tableTB.getValueAt(pilih, 4).toString();
                String program = tableTB.getValueAt(pilih, 5).toString();

                npmTextField.setText(npm);
                namaTextField.setText(nama);
                jkCB.setSelectedIndex(jenis_kelamin.equals("L") ? 1 : 2);
                prodiCB.setSelectedIndex(prodi.equals("Teknik Informatika") ? 1 : 2);
                semesterCB.setSelectedItem(semester);
                programCB.setSelectedItem(program);

                tambahButton.setEnabled(true); // Aktifkan tombol jika diperlukan
            } else {
                // Tampilkan pesan atau lakukan sesuatu jika tidak ada baris yang dipilih
            }
        } catch (Exception e) {
            e.printStackTrace();
            // Tampilkan pesan atau lakukan sesuatu jika terjadi kesalahan
        }
    }//GEN-LAST:event_tableTBMouseClicked

